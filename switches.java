public class switches {
    public static void main(String args[]){
        int a = 'a';
        switch(a) {
            case 'd': System.out.println("butter chicken");
            case 'b': System.out.println("Biriyani");
            case 'a': System.out.println("Naan");
            default: System.out.println(" we are dreaming" );  
        }
    }
}
